function addTriggers() {
  let deleteTriggers = (triggers) => {
    for (var i in triggers) {
      ScriptApp.deleteTrigger(triggers[i]);
    }
  };
  deleteTriggers(ScriptApp.getProjectTriggers());

  ScriptApp.newTrigger('lockBindings').forSpreadsheet(sheetId).onEdit().create();
  ScriptApp.newTrigger('markPresent').forSpreadsheet(sheetId).onOpen().create();
}
