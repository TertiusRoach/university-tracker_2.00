function markPresent() {
  let present = `${new Date()}`.slice(0, 15);
  let dateRows = [3, 14, 25, 36, 47, 58, 69, 80, 91, 102, 113, 124, 135, 146, 157, 168, 179, 190, 201, 212, 223, 234, 245, 256, 267, 278, 289, 300, 311, 322, 333];

  //--|▼| Convert month to integer |▼|--//
  const monthNumber = (month) => {
    switch (month) {
      case 'Jan':
        return '01';
      case 'Feb':
        return '02';
      case 'Mar':
        return '03';
      case 'Apr':
        return '04';
      case 'May':
        return '05';
      case 'Jun':
        return '06';
      case 'Jul':
        return '07';
      case 'Aug':
        return '08';
      case 'Sep':
        return '09';
      case 'Oct':
        return '10';
      case 'Nov':
        return '11';
      case 'Dec':
        return '12';
    }
  };

  //--|▼| Clear boolean values for H column |▼|--//
  for (let i = 0; i < dateRows.length; i++) {
    greenBookings.getRange(`H${dateRows[i]}`).setValue('FALSE');
  }

  for (let i = 0; i < dateRows.length; i++) {
    //--|▼| Get the date for each paragraph |▼|--//
    var paragraph = `${greenBookings.getRange(`C${dateRows[i]}`).getValue()}`.slice(0, 15);
    var paragraphInteger = parseInt(`${paragraph.split(' ')[3]}${monthNumber(paragraph.split(' ')[1])}`);
    var presentInteger = parseInt(`${present.split(' ')[3]}${monthNumber(present.split(' ')[1])}`);

    if (paragraphInteger > presentInteger) {
      //--|►| Break loop for all future bookings |◄|--//
      break;
    } else if (paragraph !== present) {
      greenBookings.getRange(`H${dateRows[i]}`).setValue('TRUE');
    } else if (paragraph === present) {
      //--|►| Break loop when done |◄|--//
      break;
    }
  }

  //--|▼| Filter Column to marked specifications |▼|--//
  let filter = greenBookings.getRange('A2').getFilter();
  let criteria = SpreadsheetApp.newFilterCriteria().setHiddenValues(['*']).build();
  filter.setColumnFilterCriteria(1, criteria);
}
